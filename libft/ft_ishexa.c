#include "libft.h"

int		ft_ishexa(char c)
{
	return ((c == 'A') || (c == 'B') || (c == 'C')\
		|| (c == 'D') || (c == 'E') || (c == 'F')\
		|| (c == 'a') || (c == 'b') || (c == 'c')\
		|| (c == 'd') || (c == 'e') || (c == 'f')\
		|| ft_isdigit(c));
}
