#include "libft.h"

int			ft_atoii(char *nbr, int *i)
{
	int nb;

	nb = 0;
	if (nbr[*i] == ' ')
		(*i)++;
	if (nbr[*i] == '+')
		(*i)++;
	while (nbr[*i] && (nbr[*i] >= '0' && nbr[*i] <= '9'))
	{
		nb = nb * 10 + (nbr[*i] - '0');
		(*i)++;
	}
	if (!nbr[*i])
		return (nb);
	else if (nbr[*i] == ' ')
		return (nb);
	else
		return (-1);
}
