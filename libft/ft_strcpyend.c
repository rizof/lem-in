#include "libft.h"

char	*ft_strcpyend(char *s1, char *s2)
{
	int i;

	i = 0;
	if (!s2 || !s1)
		return (s2);
	while (s1[i])
	{
		s2[i] = s1[i];
		i++;
	}
	return (s2);
}
